function parseLevelStrings() {
    var results = [];
    for (var l = 0; l < levelStrings.length; l++) {
        var index = 0;
        results[l] = [];
        for (var y = 0; y < levelStrings[l].length; y++) {
            for (var x = 0; x < levelStrings[l][y].length; x++) {
                results[l][index++] = parseInt(levelStrings[l][y].charAt(x));
            }
        }
    }
    return results;
}

